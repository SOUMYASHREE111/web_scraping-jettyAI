from datetime import datetime


def get_author_score(author):

    if author and str(author).strip():
        return 0.8

    return 0.3


def get_domain_score(url):

    url = url.lower()

    if "pubmed" in url:
        return 1.0

    elif "youtube" in url or "youtu.be" in url:
        return 0.7

    elif ".edu" in url:
        return 0.9

    elif ".gov" in url:
        return 0.95

    elif ".org" in url:
        return 0.8

    elif ".com" in url:
        return 0.6

    return 0.4


def get_citation_score(citation_count):

    if citation_count >= 20:
        return 1.0

    elif citation_count >= 10:
        return 0.8

    elif citation_count >= 5:
        return 0.6

    elif citation_count > 0:
        return 0.4

    return 0.2


def get_recency_score(year):

    try:

        current_year = datetime.now().year

        age = current_year - int(year)

        if age <= 1:
            return 1.0

        elif age <= 3:
            return 0.8

        elif age <= 5:
            return 0.6

        else:
            return 0.3

    except:
        return 0.5


def get_disclaimer_score(text):

    text = text.lower()

    keywords = [
        "educational purposes",
        "consult a doctor",
        "medical advice",
        "healthcare professional"
    ]

    for keyword in keywords:

        if keyword in text:
            return 1.0

    return 0.0


def calculate_trust_score(
    source_url,
    author,
    published_year,
    citation_count,
    content_text
):

    author_score = get_author_score(author)

    domain_score = get_domain_score(source_url)

    citation_score = get_citation_score(citation_count)

    recency_score = get_recency_score(published_year)

    disclaimer_score = get_disclaimer_score(content_text)

    trust_score = (
        0.25 * author_score +
        0.25 * domain_score +
        0.20 * citation_score +
        0.20 * recency_score +
        0.10 * disclaimer_score
    )

    return round(trust_score, 2)

