def scrape_pubmed(pubmed_id):

    handle = Entrez.efetch(
        db="pubmed",
        id=pubmed_id,
        rettype="abstract",
        retmode="xml"
    )

    records = Entrez.read(handle)

    article = records["PubmedArticle"][0]

    article_data = article["MedlineCitation"]["Article"]

    # Title
    title = str(article_data.get("ArticleTitle", ""))

    # Authors
    authors = []

    if "AuthorList" in article_data:

        for author in article_data["AuthorList"]:

            lastname = author.get("LastName", "")
            firstname = author.get("ForeName", "")

            authors.append(
                f"{firstname} {lastname}".strip()
            )

    # Abstract
    abstract = ""

    if "Abstract" in article_data:

        abstract_parts = article_data["Abstract"]["AbstractText"]

        abstract = " ".join(
            map(str, abstract_parts)
        )

    # Journal

    journal = article_data["Journal"]["Title"]

    # Publication Year

    year = ""

    try:

        year = article_data["Journal"]["JournalIssue"][
            "PubDate"
        ]["Year"]

    except:

        year = "Unknown"

    data = {
        "source_url": f"https://pubmed.ncbi.nlm.nih.gov/{pubmed_id}/",
        "source_type": "pubmed",
        "author": authors,
        "published_date": year,
        "language": "English",
        "region": "Unknown",
        "topic_tags": [],
        "trust_score": 0.9,
        "content_chunks": [abstract],
        "title": title,
        "journal": journal
    }

    return data

