import json
import re
from yt_dlp import YoutubeDL
from youtube_transcript_api import YouTubeTranscriptApi


def extract_video_id(url):
    pattern = r"(?:v=|\/)([0-9A-Za-z_-]{11}).*"
    match = re.search(pattern, url)

    if match:
        return match.group(1)

    return None


def scrape_youtube(video_url):

    ydl_opts = {
        "quiet": True
    }

    # Metadata
    with YoutubeDL(ydl_opts) as ydl:
        info = ydl.extract_info(video_url, download=False)

    title = info.get("title")
    channel = info.get("uploader")
    publish_date = info.get("upload_date")
    description = info.get("description")

    # Transcript
    video_id = extract_video_id(video_url)

    transcript_text = ""

    try:
        transcript = YouTubeTranscriptApi.get_transcript(video_id)

        transcript_text = " ".join(
            [item["text"] for item in transcript]
        )

    except Exception:
        transcript_text = "Transcript unavailable"

    # Content chunks
    chunks = []

    chunk_size = 500

    for i in range(0, len(transcript_text), chunk_size):
        chunks.append(
            transcript_text[i:i+chunk_size]
        )

    data = {
        "source_url": video_url,
        "source_type": "youtube",
        "Channel_name": channel,
        "Video_publish_date": publish_date,
        "language": "unknown",
        "region": "unknown",
        "topic_tags": [],
        "trust_score": 0.7,
        "content_chunks": chunks,
        "title": title,
        "video_description": description
    }

    return data

video_url = input("Enter YouTube URL: ")

result = scrape_youtube(video_url)

print(json.dumps(result, indent=4))

